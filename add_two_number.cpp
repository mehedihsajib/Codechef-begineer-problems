///////////////////////////////////////

#include <bits/stdc++.h>
using namespace std;

int main() {

	// Declare essential variables.
	int t, a, b, sum;

	// Taking number of test case;
	cin >> t;

	while(t--) {
		// Taking input while t false.
	  cin >> a >> b;

	  // Add two numbers and print output.
	  sum = a + b;
	  cout << sum << endl;
	}

	return 0;
}

