#include <bits/stdc++.h>
using namespace std;

int main() {

	// Taking number test case.
	int t;
	cin >> t;

	while(t--) {
		// Taking n while t is true.
		int a, b;
	  cin >> a >> b;
	  
	  // Calculate reminder and print;
	  cout << a%b << endl;
	}

	return 0;
}


